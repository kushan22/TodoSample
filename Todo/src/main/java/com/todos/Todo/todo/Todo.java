package com.todos.Todo.todo;

import java.util.Date;

public class Todo {

	private long id;
	private String username;
	private String todoDescription;
	private Date targetDate;
	private boolean isDone;
	
	protected Todo() {
		
	}

	public Todo(long id, String username,String todoDescription, Date targetDate, boolean isDone) {
		super();
		this.id = id;
		this.username = username;
		this.todoDescription = todoDescription;
		this.targetDate = targetDate;
		this.isDone = isDone;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}
	
	public String getTodoDescription() {
		return todoDescription;
	}
	
	public void setTodoDescription(String todoDescription) {
		this.todoDescription = todoDescription;
	}

	public Date getTargetDate() {
		return targetDate;
	}

	public void setTargetDate(Date targetDate) {
		this.targetDate = targetDate;
	}

	public boolean isDone() {
		return isDone;
	}

	public void setDone(boolean isDone) {
		this.isDone = isDone;
	}
	
	

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + (int) (id ^ (id >>> 32));
		result = prime * result + (isDone ? 1231 : 1237);
		result = prime * result + ((targetDate == null) ? 0 : targetDate.hashCode());
		result = prime * result + ((todoDescription == null) ? 0 : todoDescription.hashCode());
		result = prime * result + ((username == null) ? 0 : username.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Todo other = (Todo) obj;
		if (id != other.id)
			return false;
		if (isDone != other.isDone)
			return false;
		if (targetDate == null) {
			if (other.targetDate != null)
				return false;
		} else if (!targetDate.equals(other.targetDate))
			return false;
		if (todoDescription == null) {
			if (other.todoDescription != null)
				return false;
		} else if (!todoDescription.equals(other.todoDescription))
			return false;
		if (username == null) {
			if (other.username != null)
				return false;
		} else if (!username.equals(other.username))
			return false;
		return true;
	}

}
